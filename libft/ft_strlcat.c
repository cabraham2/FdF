#include "libft.h"

size_t	ft_strlcat(char *dest, const char *src, size_t size)
{
	size_t	i;
	size_t	len;

	i = 0;
	len = 0;
	while (dest[len] && (len < size))
		len++;
	while (src[i] && size && ((len + i) < (size - 1)))
	{
		dest[len + i] = src[i];
		i++;
	}
	if (len < size)
		dest[len + i] = '\0';
	i = 0;
	while (src[i])
		i++;
	return (len + i);
}
